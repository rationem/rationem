package com.rationem.util.helper;

/**
 * Structure for unique post codes
 * @author Chris
 */
public class PostCodeHouseNum {
 
 private String  postCode;
 private String houseNumName;

 public PostCodeHouseNum() {
 }

 public String getHouseNumName() {
  return houseNumName;
 }

 public void setHouseNumName(String houseNumName) {
  this.houseNumName = houseNumName;
 }

 public String getPostCode() {
  return postCode;
 }

 public void setPostCode(String postCode) {
  this.postCode = postCode;
 }
 
 
 
}
